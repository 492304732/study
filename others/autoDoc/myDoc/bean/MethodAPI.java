package bean;

public class MethodAPI{
    private String methodName;
    private String paramAPI;
    private String returnAPI;

    public String getMethodName() {
        return methodName;
    }

    public void setMethodName(String methodName) {
        this.methodName = methodName;
    }

    public String getParamAPI() {
        return paramAPI;
    }

    public void setParamAPI(String paramAPI) {
        this.paramAPI = paramAPI;
    }

    public String getReturnAPI() {
        return returnAPI;
    }

    public void setReturnAPI(String returnAPI) {
        this.returnAPI = returnAPI;
    }
}